export function createV2ReportBlockId() {
  if (typeof crypto !== 'undefined' && typeof crypto.randomUUID === 'function') {
    return crypto.randomUUID()
  }

  return `v2_report_block_${Date.now()}_${Math.random().toString(36).slice(2, 10)}`
}

